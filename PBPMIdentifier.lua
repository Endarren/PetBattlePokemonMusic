function PetBattlePokemonMusic:DetermineBattleType ()
	local battleType, opponentName = PetBattlePokemonMusic:IdentifyBattleType()
	--print("This is a ".. battleType.." against ".. opponentName)
	if C_PetBattles.IsWildBattle() == true then
		return L["WILD"] 
	else
		if C_PetBattles.IsPlayerNPC(2) == true then
			return L["TAMER"]
		else
			return "PvP"
		end
	end
end
function PetBattlePokemonMusic:IdentifyBattleType()
	--PetBattlePokemonMusic:CreateTamerIdentifierData()
	--Do a scan on the enemy team to see if they match any known teams.
	local possibleTamerName = PetBattlePokemonMusic:GetTamerName ()
	if possibleTamerName ~= L["UNKNOWN"] then
		--Return the type and match if found.
		return L["TAMER"], possibleTamerName
	end
	--Check if the battle is against a wild pet.
	if C_PetBattles.IsWildBattle() == true then
		--Return type and lead pet if so
		return L["WILD"], C_PetBattles.GetName(2,1)
	end
	--Check if the battle is not against a human player.
	if C_PetBattles.IsPlayerNPC(2) == true then
		--If opponent is NPC, return type as tamer and opponent name as UNKNOWN.
		return L["TAMER"], L["UNKNOWN"]
	else
		--Return type as PvP and use player detection info.
		return "PvP", duelistName
	end
end
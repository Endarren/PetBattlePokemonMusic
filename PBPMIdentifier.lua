local duelistName = ""
function PetBattlePokemonMusic:ClearDuelistName()
	duelistName = ""
end
function PetBattlePokemonMusic:DuelCheck(a1, a2)
	if a1 ~= nil then
		if a1.value ~= nil then
			if strmatch( a1.value, "PET_BATTLE_PVP_DUEL") ~= nil then
				name, realm = UnitName("target")
				if realm == nil then
					realm = GetRealmName()
				end
				duelistName = name.."-"..realm
			end
		end
	end
	
end
function PetBattlePokemonMusic:GetpveDB()
	return pveDB
end
function PetBattlePokemonMusic:CreateTamerIdentifierData()

	for i = 1, C_PetBattles.GetNumPets(2) do
		print("[\""..C_PetBattles.GetName(2,i).."\"]={Name = \""..C_PetBattles.GetName(2,i)..
												"\",Level = "..C_PetBattles.GetLevel(2,i)..
													", Quality= "..C_PetBattles.GetBreedQuality(2,i)..
														",Type= "..C_PetBattles.GetPetType (2,i)..
														"}"
			)
	end
end
